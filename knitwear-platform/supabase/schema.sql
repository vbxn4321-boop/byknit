-- 3. PATTERNS (Marketplace)
create table public.patterns (
  id uuid default uuid_generate_v4() primary key,
  designer_id uuid references public.profiles(id),
  type text check (type in ('internal_pdf', 'external_link')) not null,
  title jsonb not null default '{}'::jsonb, -- { "en": "...", "ko": "..." }
  description jsonb default '{}'::jsonb,
  price_usd numeric default 0,
  file_path text, -- Supabase Storage path (null for external)
  origin_url text, -- For external links
  thumbnail_url text,
  images text[],
  is_watermarked boolean default false,
  category text, 
  subcategory text,
  difficulty text,
  craft_type text check (craft_type in ('knitting', 'crochet')), -- New: Craft Type
  yarn_weight text,
  yardage integer, -- e.g. 1200 (in meters)
  needles text,      -- e.g. '4.0mm, 3.5mm'
  gauge text,        -- e.g. '22sts/30rows'
  created_at timestamp with time zone default timezone('utc'::text, now())
);

-- RLS for Patterns
alter table public.patterns enable row level security;
create policy "Patterns are viewable by everyone." on patterns for select using (true);
create policy "Designers can insert own patterns." on patterns for insert with check (auth.uid() = designer_id);

