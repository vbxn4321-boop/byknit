-- Function to handle new user signup
create or replace function public.handle_new_user() 
returns trigger as $$
begin
  insert into public.profiles (id, email, display_name)
  values (new.id, new.email, split_part(new.email, '@', 1));
  return new;
end;
$$ language plpgsql security definer;

-- Trigger for new user
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

-- 7. REVIEWS (Verified Buyers Only)
create table public.reviews (
  id uuid default uuid_generate_v4() primary key,
  user_id uuid references public.profiles(id) not null,
  pattern_id uuid references public.patterns(id) not null,
  rating integer not null check (rating >= 1 and rating <= 5),
  content text not null check (length(content) >= 10),
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(user_id, pattern_id) -- One review per user per pattern
);

-- RLS for Reviews
alter table public.reviews enable row level security;
create policy "Reviews are viewable by everyone." on reviews for select using (true);
create policy "Users can insert reviews for purchased patterns." on reviews for insert with check (
  auth.uid() = user_id and
  exists (
    select 1 from public.orders
    where orders.user_id = auth.uid()
    and orders.pattern_id = reviews.pattern_id
    and orders.status = 'paid'
  )
);
create policy "Users can update own reviews." on reviews for update using (auth.uid() = user_id);
create policy "Users can delete own reviews." on reviews for delete using (auth.uid() = user_id);

-- 8. FOLLOWS
create table public.follows (
  follower_id uuid references public.profiles(id) not null,
  following_id uuid references public.profiles(id) not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  primary key (follower_id, following_id),
  check (follower_id <> following_id)
);

-- RLS for Follows
alter table public.follows enable row level security;
create policy "Follows are viewable by everyone." on follows for select using (true);
create policy "Users can follow others." on follows for insert with check (auth.uid() = follower_id);
create policy "Users can unfollow." on follows for delete using (auth.uid() = follower_id);

-- 9. CREDIT TRANSACTIONS (Coin Economy)
create table public.credit_transactions (
  id uuid default uuid_generate_v4() primary key,
  user_id uuid references public.profiles(id) not null,
  amount integer not null, -- Positive for earning, negative for spending
  type text not null check (type in ('earning', 'spending', 'initial_bonus', 'refund')),
  description text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- RLS for Credit Transactions
alter table public.credit_transactions enable row level security;
create policy "Users can view own transactions." on credit_transactions for select using (auth.uid() = user_id);

-- Function to update profile credits automatically
create or replace function public.update_profile_credits() 
returns trigger as $$
begin
  update public.profiles 
  set credits = credits + new.amount
  where id = new.user_id;
  return new;
end;
$$ language plpgsql security definer;

-- Trigger for credit updates
create trigger on_credit_transaction_created
  after insert on public.credit_transactions
  for each row execute procedure public.update_profile_credits();

